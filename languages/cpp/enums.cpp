// An enum (enumeration) is a way to create your own data type that can have only a fixed set of values.

// Instead of using numbers like 0, 1, 2, you use meaningful names.

enum class Color { red, blue, green };
// Enums are basically small integers (internally)
//Behind the scenes, enums are stored as integers, but you don’t deal with numbers directly.


//What is a “plain” enum?
enum Color { red, green, blue };
int col = green;  // 1

